package typecasting;

public class Primitive {

	public static void main(String[] args) {
		
		//widening casting
		int a = 100;
		System.out.println(a);
		double b = a;
		System.out.println(b);
		
		//narrowing casting;
		double c = 5.4;
		System.out.println(c);
		int d = (int)c;
		System.out.println(d);
		
		
	}

}
